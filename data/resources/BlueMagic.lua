-- BLU spell -> trait + stat-bonus table.
--
-- Source of truth: GearInfo's Blue_Mage_Spells.lua (id-keyed) merged
-- with OmniWatch's snake_case trait-key naming convention. GearInfo's
-- `points` and `trait` fields cross-checked against the prior name-
-- keyed Blu_spell_traits.lua at zero divergence on overlapping spells.
--
-- Schema per name-keyed entry:
--   ["<spell name>"] = {
--       id    = N,                          -- spell id (windower res match)
--       level = N,                          -- level requirement (info only)
--       <trait_key> = <points>,             -- one trait contribution; omitted
--                                           -- when GearInfo's trait = N/A.
--                                           -- Trait keys match
--                                           -- OW_BLU_TRAIT_TABLES in
--                                           -- OmniWatch.lua: dw, da,
--                                           -- triple_attack, fast_cast,
--                                           -- mab, mdb, etc.
--       stats  = {str=N, dex=N, vit=N,      -- per-spell attribute bonuses
--                 agi=N, int=N, mnd=N,      -- (only nonzero shown). Lower-
--                 chr=N},                   -- case keys match the stats[]
--                                           -- dict naming in OmniWatch.
--       vitals = {hp=N, mp=N},              -- per-spell HP/MP modifiers
--                                           -- (only nonzero shown). NOT
--                                           -- currently wired to the
--                                           -- stat aggregator — kept here
--                                           -- for future use.
--   }
--
-- Spell-name keys come from GearInfo's `en` field, mirroring
-- windower res.spells[*].english. OmniWatch's case-insensitive index
-- (OW_BLU_SPELLS_LC) handles casing differences across sources.
--
-- ── Trait notes (matches OW_BLU_TRAIT_TABLES in OmniWatch.lua):
--   dw             8 pts -> 10%, 16 -> 15%, 24 -> 25%, 32 -> 30%,
--                  40 -> 35% (DW V, gated on 100+ JP gift),
--                  48 -> 37% (DW VI, gated on 1200+ JP gift)
--   da             8 pts -> +7%   Double Attack    (gifts do NOT apply)
--   triple_attack  8 pts -> +5%   Triple Attack    (gifts do NOT apply)
--   Per BG-wiki Blue_Mage_Job_Traits, GearInfo's "Double/Triple Attack"
--   trait label corresponds to spells that grant Double Attack only,
--   while "Triple Attack" labels spells that grant Triple Attack only —
--   these are distinct traits with different spell sets. They are mapped
--   to `da` and `triple_attack` here so OmniWatch's stats panel can show
--   them separately.
--
-- ── BLU job-point gifts that boost trait points:
--   100 JP gift:  +8 trait points to each equipped trait
--   1200 JP gift: another +8 trait points (stacks; gated on the trait
--                 already having >=8 base points equipped)
--   Gift-EXEMPT traits (per BG-wiki + bluguide source): Auto-Refresh,
--   Double Attack, Triple Attack, Gilfinder, Rapid Shot, Zanshin,
--   Killer Traits, Resist Traits.
--
-- ── _by_id fallback at bottom of file:
--   M._by_id[<spell_id>] = M[<spell_name>]
--   For consumers that have a spell id in hand and want the entry
--   without going through res.spells. The OmniWatch BLU aggregator
--   primarily looks up by name; this is a convenience fallback.

